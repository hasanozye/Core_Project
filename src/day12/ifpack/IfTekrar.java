package day12.ifpack;

public class IfTekrar {
    public static void main(String[] args) {
        int k1 = 4;
        int k2 = 8;


        if (k1 > k2) {
            System.out.println("K1 büyüktür.");
        } else if (k1 == k2) {
            System.out.println("K1 ve K2 eşittir.");
        } else {
            System.out.println("K1 küçüktür.");
        }
    }
}
